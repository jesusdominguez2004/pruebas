# Algoritmo "Positivo o negativo"

# Captura de información
num = eval(input("Ingrese un número: "))

# Iteración 1
if num > 0:
    print(num, " es un número positivo")
    print("Fin del algoritmo")
# Iteración 2
if num < 0:
    print(num, " es un número negativo")
    print("Fin del algoritmo")
# Iteración 3
if num == 0:
    print(num, " es un número neutral")
    print("Fin del algoritmo")