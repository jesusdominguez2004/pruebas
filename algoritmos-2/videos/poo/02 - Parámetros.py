def saludar(nombre, apellido):
    print(f"¡Hola mundo! {nombre} {apellido}")

saludar("José", "Mendoza") # Llamado 1
saludar("Germán", "Fonseca") # Llamado 2
saludar("Jesús", "Domínguez") # Llamado 3


def suma(num1, num2):
    print(f"{num1} + {num2} = {num1 + num2}")

suma(30, 2) # Llamado 1
suma(70, 3) # Llamado 1
suma(10, 5) # Llamado 1
